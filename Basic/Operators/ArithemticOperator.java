package Basic.Operators;

public class ArithemticOperator {
    public static void main(String args [] ){
        int a =50, b=10;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
