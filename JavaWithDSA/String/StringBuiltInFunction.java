package JavaWithDSA.String;

public class StringBuiltInFunction {
    public static void main(String args[]){
        String s1 = "JavaProgramming";
        System.out.println(s1);

        s1 = s1.toUpperCase();
        System.out.println(s1); 

        s1 = s1.toLowerCase();
        System.out.println(s1);

        s1 = s1.concat("rocks");
        System.out.println(s1); 

        s1.toUpperCase();
        System.out.println(s1);

    }
}