package JavaWithDSA.Sorting;

import java.util.Scanner;

public class SelectionSort {
    public static void main(String [] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int arr[] = new int[n];
        for(int i=0; i<n; i++){
            arr[i] = sc.nextInt();
        }
        for (int i = 0; i < n-1; i++) {
            int m = i;
            for (int j = i+1; j < n; j++) {
                if(arr[j] < arr[i]){
                    m = j;
                }
            }
            int t = arr[i];
            arr[i] = arr[m];
            arr[m] = t;
        }

        for(int i : arr){
            System.out.print(i + " ");
        }
    }
}
